fun main() {
    val number1 = 20;
    val number2 = 2;

    val sum: Int = number1 + number2;
    println("+ Sum $sum");

    val sub: Int = number1 - number2;
    println("+ Sum $sub");

    val mult: Int = number1 * number2;
    println("+ Sum $mult");

    val div: Int = number1 / number2;
    println("+ Sum $div");
}